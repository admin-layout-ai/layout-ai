/**
 * Lookup council name by state and postcode
 * @param state - Australian state code (NSW, VIC, etc.)
 * @param postcode - 4-digit Australian postcode
 * @returns Council name or empty string if not found
 */
export function lookupCouncil(state: string, postcode: string): string {
  if (!state || !postcode || postcode.length !== 4) return '';
  const stateCouncils = COUNCIL_LOOKUP[state.toUpperCase()];
  if (!stateCouncils) return '';
  return stateCouncils[postcode] || '';
}

/**
 * Get all councils for a state
 * @param state - Australian state code
 * @returns Array of unique council names
 */
export function getCouncilsForState(state: string): string[] {
  const stateCouncils = COUNCIL_LOOKUP[state.toUpperCase()];
  if (!stateCouncils) return [];
  return [...new Set(Object.values(stateCouncils))].sort();
}

/**
 * Validate Australian postcode
 * @param postcode - Postcode to validate
 * @returns true if valid 4-digit postcode
 */
export function isValidAustralianPostcode(postcode: string): boolean {
  return /^\d{4}$/.test(postcode);
}
